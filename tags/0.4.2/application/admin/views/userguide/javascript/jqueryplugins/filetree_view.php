<h1>Filetree</h1>
	<p>
		jQuery File Tree is a configurable, AJAX file browser plugin for jQuery. 
		You can create a customized, fully-interactive file tree with as little as one line of JavaScript code. 
	</p>
	<p>
		Currently, server-side connector scripts are available for PHP, ASP, ASP.NET, JSP, and Lasso. 
		If youâ€™re a developer, you can easily make your own connector to work with your language of choice. 
	</p>
	
<h2>Features</h2>
	<ul>
		<li>Produces valid, semantic XHTML</li>
		<li>Fully customizable via CSS</li>
		<li>Ability to style icons based on file extension</li>
		<li>Uses AJAX to fetch file information on the fly</li>
		<li>Easy to configure and implement</li>
		<li>Includes connector scripts for PHP and ASP.NET (C#)</li>
		<li>Supports custom connector scripts for extended functionality</li>
		<li>Customizable expand/collapse event</li>
		<li>Customizable expand/collapse speeds</li>
		<li>Supports jQuery easing functions</li>
		<li>Single- and multi-folder views</li>
		<li>Configurable load message</li>
	</ul>

<h2>Compatibility</h2>
	<p>jQuery File Tree works in all browsers supported by jQuery. It has been fully tested in:</p>
	<ul>
		<li>Internet Explorer 6 & 7</li>
		<li>Firefox 2 & 3</li>
		<li>Safari 3</li>
		<li>Chrome</li>
		<li>Opera 9</li>
	</ul>

