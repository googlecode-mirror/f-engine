<h1>CSS diagnostics</h1>
<p>
	CSS Diagnostics allows users to find deprecated XHTML elements and attributes using CSS. 
	It highlights all deprecated attributes in a solid border while highlighting all deprecated elements in a dotted border. 
	Also uses different colors for different meanings.
</p>
<h2>How do you use CSS Diagnostics?</h2>
<p>
	Just include the css file into the page you want to validate.
</p>
