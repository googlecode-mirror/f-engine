<h1>Auto Expanding Text area</h1>
	<p>
		This plugin makes it easy to have auto-growing textareas. 
		Meaning, if you have a textarea, as the user types it will expand vertically to accommodate the size of the entry. 
	</p>
	<p>
		This was inspired by Facebook's auto-expanding text areas. 
	</p>


