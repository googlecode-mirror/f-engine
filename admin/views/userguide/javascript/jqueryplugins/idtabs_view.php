<h1>Idtabs</h1>
	<p>
		idTabs is a plugin for jQuery.
		It makes adding tabs into a website super simple.
		But it can also open the door to endless possiblities.
	</p>
<h2>Getting started </h2>
	<p>Just include the file into your page.</p>
	<p>Now any &lt;a href="#tab"&gt; inside of an element with class="idTabs"
		will show the element with id="tab".</p>
	
