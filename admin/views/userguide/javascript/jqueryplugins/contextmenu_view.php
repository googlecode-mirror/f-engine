<h1>Context menu</h1>
	<p>
		jQuery Context Menu is a context menu plugin for jQuery. 
		It was designed to make implementing context menu functionality easy and requires minimal effort to configure.
	</p>
	<p>
		There is another context menu for jQuery written by Chris Domigan. 
		These two projects are completely unrelated and no source code was shared from Chris’ project.	
	</p>
	<p>
		Why reinvent the wheel? The goal of this plugin is to streamline the way actions are binded to menu items and to use 100% CSS for styling. 
		Keyboard shortcuts were added for navigating the menu once it’s open, and there are five methods to allow you to control and clean-up context menus on the fly.	
	</p>
<h2>Features</h2>
	<ul>
		<li>Valid XHTML</li>
		<li>Fully customizeable via CSS</li>
		<li>Easy to configure and implement</li>
		<li>Degrades gracefully</li>
		<li>Keyboard shortcuts to maximize accessibility</li>
		<li>Ability to enable/disable menu items on the fly</li>
	</ul>

<h2>Compatibility</h2>
	<p>jQuery Context Menu works in all browsers supported by jQuery. It has been fully tested in:</p>
	<ul>
		<li>Internet Explorer 6 & 7</li>
		<li>Firefox 2 & 3</li>
		<li>Safari 3</li>
		<li>Chrome (beta)</li>
		<li>Opera 9.5*</li>
	</ul>
	<p>
		*Opera 9.5 has an option to allow scripts to detect right-clicks, but it is disabled by default. 
		Furthermore, Opera still doesn’t allow JavaScript to disable the browser’s default context menu which causes a usability conflict.
	</p>

