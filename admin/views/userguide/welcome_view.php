<h1>Welcome to F-engine</h1>
<p>
    F-engine is anApplication Development Framework based on <a href="http://codeigniter.com/">codeigniter</a> - a toolkit - for people who build web sites using PHP.
    Its goal is to enable you to develop projects much faster than you could if you were writing code
    from scratch, by providing a rich set of libraries and web based applications for commonly needed tasks, as well as a simple interface and
    logical structure to access these libraries. F-engine lets you creatively focus on your project by
    minimizing the amount of code needed for a given task.
</p>
<h2>Who is F-engine For?</h2>
<p>
    F-engine is right for you if:
</p>
<ul>
    <li>
        You want a framework with a small footprint.
    </li>
    <li>
        You need exceptional performance.
    </li>
    <li>
        You need broad compatibility with standard hosting accounts that run a variety of PHP versions and configurations.
    </li>
    <li>
        You want a framework that requires nearly zero configuration.
    </li>
    <li>
        You want a framework that does not require you to use the command line.
    </li>
    <li>
        You want a framework that does not require you to adhere to restrictive coding rules.
    </li>
    <li>
        You are not interested in large-scale monolithic libraries like PEAR.
    </li>
    <li>
        You do not want to be forced to learn a templating language (although a template parser is optionally available if you desire one).
    </li>
    <li>
        You eschew complexity, favoring simple solutions.
    </li>
    <li>
        You need clear, thorough documentation.
    </li>
</ul>
<h2>Server Requirements</h2>
<ul>
	<li>
        <a href="http://www.php.net/">PHP</a>
        version 5.0 or newer for project development (since new project builder requires simplexml library).
    </li>
    <li>
        <a href="http://www.php.net/">PHP</a>
        version 4.3.2 or newer for code execution.
    </li>
    <li>
        A Database is required for most web application programming. Current supported databases are MySQL (4.1+), MySQLi, MS SQL, Postgres, Oracle, SQLite, and ODBC.
    </li>
</ul>