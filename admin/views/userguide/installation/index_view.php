<h1>Installation Instructions</h1>
<p>
    F-engine is installed in three steps:
</p>
<ol>
    <li>
        Unzip the package.
    </li>
    <li>
        Upload the F-engine folders and files to your server.  Normally the index.php file will be at your root.
    </li>
    <li>
        Open your browser and follow "create a new project" assistant  
    </li>
</ol>
<p>
    That's it!
</p>
